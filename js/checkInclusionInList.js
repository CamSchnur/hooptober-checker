function checkInclusionInList(movieURL, listType)
{
    switch(listType)
    {
        case "robertEnglundMovies":
            if(getRobertEnglundMovies().find(e => e == movieURL))
            {
                return true;
            }
            break;
        case "badDraculaMovies":
            if(getBadDraculaMovies().find(e => e == movieURL))
            {
                return true;
            }
            break;
        case "peterCushingMovies":
            if(getPeterCushingMovies().find(e => e == movieURL))
            {
                return true;
            }
            break;
        case "queerHorrorMovies":
            if(getQueerHorrorMovies().find(e => e == movieURL))
            {
                return true;
            }
            break;
    }
    return false;
}

